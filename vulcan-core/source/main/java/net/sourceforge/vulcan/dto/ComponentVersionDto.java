package net.sourceforge.vulcan.dto;

public class ComponentVersionDto extends BaseDto {
	public enum ComponentType { Package, Plugin };
	
	private ComponentType type;
	private String id;
	private String name;
	private String version;

	public ComponentVersionDto(ComponentType type) {
		super();
		this.type = type;
	}
	public ComponentType getType() {
		return type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
}
